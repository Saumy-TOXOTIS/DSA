void Bridges_in_graph(ll node,ll parent)
{
    visited[node] = 1;
    time_insertion[node] = lowest_time[node] = timer;
    timer++;
    for(auto item : graph[node])
    {
        if(item == parent)
        {
            continue;
        }
        if(!visited[node])
        {
            Bridges_in_graph(item,node);
            lowest_time[node] = min(lowest_time[node],lowest_time[item]);
            if(lowest_time[item] > time_insertion[node])
            {
                bridges.psb({item,node});
            }
        }
        else
        {
            lowest_time[node] = min(lowest_time[node],lowest_time[item]);
        }
    }
}

